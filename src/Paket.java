public class Paket {

    int amount;
    int weight;
    int length;
    int width;
    int height;


    public Paket(int amount, int weight, int length, int width, int height) {
        this.amount = amount;
        this.weight = weight;
        this.length = length;
        this.width = width;
        this.height = height;
    }
}
